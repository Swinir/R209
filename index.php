Ce module est la suite du module M1106 (Initiation au développement 
Web) vu au semestre 1. Son objectif est la création de sites web
mettant en œuvre des petits scripts côtés serveur afin de rendre 
<strong>dynamique</strong> le contenu des pages Web.

 <h1>Rappels sur le module M1106</h1>

 <h2>Au semestre précédent, le module M1106 a permis de voir :</h2>
 <ul>
 <li>la structure de base d'une page HTML (&lt;html>,&lt;head>,&lt;body>, …)</li>
 <li>les différentes parties qui composent le corps de la page (&lt;header>, &lt;footer>, &lt;nav>, &lt;section>, …)</li>
 <li>les principales balises de mise en forme (titres, listes, tableaux, liens, mise en valeur du texte, …)</li>
 <li>la validation W3C des documents</li>
 <li>la mise en forme (feuilles de style CSS)</li>
 <li>les formulairesune introduction au langage javacript</li>
 </ul>

 <h1>Le module M2105</h1>

  Ce module de "Web Dynamique" va s'articuler autour de 2 parties :

 <ol type="1">
  <li>introduction au langage PHP : écriture de scripts, éxécutés sur le
  serveur, afin de répondre aux demandes du client (la page qui s'affiche
  dans le navigateur est "personnalisée")</li>
  <li>connexion à une base de données via MySQL</li>
 </ol>
  <br>
  Déroulement des séances de TP

  De manière générale, toutes les séances de TP seront décomposées
  en une petite interrogation (voir la partie <a href="#tp">Note de TP</a>) suivie
  d'exercices mettant directement en application les notions vues en
  cours.
  Enfin, la dernière partie de toutes les séances sera consacrée
  à l'élaboration progressive d'un <a href="#projet">mini-projet</a>.

  <h1>Évaluation du module</h1>

  3 notes permettront de calculer la moyenne du module. Pour l'instant,
  les coefficients affectés à chacune de ces notes ne sont pas
  encore déterminés.
  <h2 id="tp">Note de TP</h2>

  <strong>La plupart des séances de TP feront l'objet d'une note <span class="tresImportant">individuelle</span> sur
   20.</strong><br><br>
  Cette note sera constituée :

  <ul>
   <li>d'une petite interrogation ayant lieu en début de séance pour
    vérifier les acquis de la semaine précédente (environ 1/4 de la note)
   </li>
   <li>d'une évaluation du travail fourni tout au long de la séance (environ
    3/4 de la note)
   </li>
  </ul>

  La moyenne de ses notes constituera la note de TP.

  Il est donc évident que pour assurer une bonne note de TP, il suffit de
  relire le travail réalisé au TP précédent et travailler durant les
  séances de TP … rien de bien méchant !

  <h2 id="projet">Mini-Projet</h2>

 <strong>Le mini-projet sera réalisé en <span class="tresImportant">binôme</span>.</strong><br>
  Il sera essentiellement réalisé au cours des TP : une partie du temps
  lui sera consacré lors des premières séances, puis les séances lui
  seront entièrement dédiées. Enfin, il sera présenté et évalué
  lors de la dernière séance.

  <h2>DS final</h2>

  <strong>Un DS écrit sanctionnera le module.</strong><br>

  Il aura lieu à la fin du semestre (très probablement en semaine
  23). Sa durée sera de 1h30, 2 feuilles A4 manuscrites recto/verso
  seront autorisées (pas de photocopies, ni d'impressions).
